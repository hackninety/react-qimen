/**
 * 奇门遁甲 AI 分析 Prompt 模板：内嵌紧凑 Markdown 盘面（含流派口径与地理时间上下文），
 * 比 JSON 更省 token，适合直接粘贴给任意 LLM。
 */
export function generateQimenPrompt(chartMarkdown: string): string {
  return `你是一位精通奇门遁甲的资深术数分析师，拥有数十年实战经验。请根据以下排盘数据进行深入、专业的奇门遁甲分析。

数据说明：以下 Markdown 含排盘口径（流派/定局法/引擎）、地理与时间上下文（含真太阳时修正明细）、四柱、局数、旬首符首、值符值使、九宫（神/星/门/天地盘干/暗干/标记）与格局清单。请严格按标注的流派口径解读，勿混用他派规则。

${chartMarkdown}

## 请按以下框架逐项展开分析

### 1. 盘局总论
- 阴阳遁与局数含义、节气三元背景
- 四柱干支组合特征
- 值符值使落宫吉凶综合判断
- 总体时运评估

### 2. 九宫格局分析
- 各宫星、门、神、干组合解读（引用表格具体数据）
- 天盘地盘干支生克关系（十干克应）
- 已列格局逐一解析，并补充你识别出的其他格局（伏吟、反吟等）
- 空亡、马星、击刑、门迫、入墓标记对各宫的影响

### 3. 用神方位
- 最有利方位及原因
- 需要避开的方位及原因
- 出行、办事方位建议（结合排盘地点的地理方位）

### 4. 事业与决策
- 当前时机是否适合采取行动
- 有利的行业方向与合作对象
- 决策建议与注意事项

### 5. 财运分析
- 生门所在宫位与财运关系
- 求财方向与时机判断

### 6. 人际关系
- 六合、太阴等吉神方位解读
- 贵人方位与借力方式
- 需防范的方位

### 7. 综合建议
- 趋吉避凶的具体行动建议
- 开运方位、颜色、数字参考

要求：分析必须引用盘面中的具体宫位数据作为论据；使用专业奇门术语的同时给出通俗解释；对盘面未提供的信息（如占测事项）可列出需要求测人补充的问题。`;
}
