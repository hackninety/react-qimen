/**
 * 奇门遁甲 AI 分步断盘 Prompt：内嵌完整 Markdown 盘面
 * （口径/用神定位/机器预计算生克/格局/该占类古法/典籍参考），
 * 强制六步方法论——AI 只做综合解释，不做机械推算。
 */

export interface PromptInquiry {
  topicLabel: string;
  subject?: string;
}

export function generateQimenPrompt(chartMarkdown: string, inquiry?: PromptInquiry): string {
  const question = inquiry
    ? `本次求测：**${inquiry.topicLabel}**${inquiry.subject ? `，事由：「${inquiry.subject}」` : '（未填具体事由，请围绕该占类做一般性研判，并列出需求测人补充的关键信息）'}`
    : '本次未指定占测事项，请先做盘局总评，再列出适合此盘研判的事项方向。';

  return `你是一位精通奇门遁甲的资深术数分析师。请严格按下述六步方法论分析排盘数据，得出可操作的结论。

${question}

数据说明：以下 Markdown 已包含——排盘口径（盘类/流派/定局法/引擎）、地理与时间上下文（含真太阳时明细）、**用神定位表（已按占类自动取用并评估状态）**、九宫全息、**生克关系表（机器已精确计算，请直接引用、勿自行重推五行链）**、格局断语全文、**该占类古法原文（占法要旨）**、典籍参考断语。

${chartMarkdown}

## 请严格按以下六步展开

### 第一步 · 核对口径
确认盘类/流派/定局法与局数三元，声明按此口径解读，勿混用他派规则。

### 第二步 · 用神审察（断事之本）
以「用神定位」表为准：逐一评述各用神的落宫状态——旺衰、宫与用神生克、空亡/入墓/击刑/门迫等标记的含义、同宫神星门组合的助碍。用神空亡入墓者明言其对应事项虚而难成。

### 第三步 · 生克论断
以「生克关系」表为准（已算好，直接引用）：重点看 ①用神宫与日干宫的生克方向（谁生谁、谁克谁 → 事来就我还是我去求事）②用神所在宫的天地盘干组合 ③门迫/门制对吉凶门的增减。若有全盘伏吟/反吟/五不遇时/时干入墓，须作为全局基调优先陈述。

### 第四步 · 格局参断
结合「格局」与「典籍参考」的原文断语：吉格助益何处、凶格妨害何处，落宫是否恰在用神位。引用原文时注明格名。

### 第五步 · 古法印证与应期
以「占法要旨」的古法原文为纲，核对本盘用神状态是否符合古法所言吉凶条件，给出成败判断。应期按传统法推：用神宫地支对应的日时、马星发动之期、值使门落宫地支、用神旺相之月令——给出 1~2 个最可能的应期区间并说明推法。

### 第六步 · 结论与建议
1）直接回答所占之事：吉/凶/成/败/缓，程度与条件；2）最有利方位与时机；3）趋避建议（可行动的 2~4 条）；4）本盘信息不足之处与需求测人补充的问题。

要求：每一步都必须引用盘面中的具体数据（宫位/用神/生克/格局原文）作论据；术语给通俗解释；结论明确不含糊，但注明这是术数推演仅供参考。`;
}
